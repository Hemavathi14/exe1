
def prime_number(num):

    square=number*number
    final_value=square-1
    #print(final)
    if(final_value%24==0):
        print("It is a prime number")
    else:
        print("It is not a prime number")
number = int(input("enter the number to check prime or not (number should be > 3):"))

prime_number(num=number)
